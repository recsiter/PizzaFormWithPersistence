    private void tblPizzaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPizzaMouseClicked
        if (evt.getButton() == MouseEvent.BUTTON1 && evt.getClickCount() > 1) {
            int index = tblPizza.getSelectedRow();
            pizzaHandlerForm = new PizzaHandlerForm(this, pizzas.get(index));
            pizzaHandlerForm.addPizzaEventListener(eventListener);
            pizzaHandlerForm.setPizzaHandlingStrategy(new UpdatePizzaStrategy());
            pizzaHandlerForm.setVisible(true);
        }
    }//GEN-LAST:event_tblPizzaMouseClicked

