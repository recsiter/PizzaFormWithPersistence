    private long calculateNewId() {
        long newId = 1;
        if (!pizzas.isEmpty()) {
            //TODO: meg kene keresni a pizza listabol a legnagyobb id-t HW
            newId = pizzas.get(pizzas.size() - 1).
                    getId() + 1;
        }
        return newId;
    }
    
    private class PizzaListener implements PizzaEventListener {
        
        @Override
        public void pizzaCreated(Pizza newPizza) {
            pizzas.add(newPizza);
            drawTable();
        }
        
        @Override
        public void pizzaUpdated() {
            drawTable();
        }
        
        private void drawTable() {
            pizzaTableModel.fireTableDataChanged();
        }
        
    }

